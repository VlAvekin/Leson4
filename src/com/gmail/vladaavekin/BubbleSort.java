public class BubbleSort {

    public static void main(String[] args) {

        int array[] = {20, 1, 32, 0, 10};

        String ar[] = {"ab", "cd", "ca"};

        Arrays.sort(ar);

        for (String a: ar) {
            System.out.print(a + " ");
        }

    }

}
